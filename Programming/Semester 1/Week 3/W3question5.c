#include <stdio.h>

int main()
{
    int sum;


    // This is an addition sum
    sum = 15 + 10;
    printf("\nsum contains %d", sum);

    // This is a subtraction sum
    sum = 15 - 10;
    printf("\nsum contains %d", sum);

    // This is a multiplication form
    sum = 15 * 10;
    printf("\nsum contains %d", sum);

    // This is a division sum
    sum = 15 / 10;
    printf("\nsum contains %f", sum);

    // This is a modulus sum
    sum = 15 % 3;
    printf("\nsum contains %d", sum);

    return 0;

}